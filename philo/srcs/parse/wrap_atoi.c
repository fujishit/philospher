#include "philosophers.h"

int	wrap_atoi(const char *s, int *n)
{
	int	i;
	int	sign;

	i = 0;
	sign = -1;
	*n = 0;
	if (*s == '-')
		sign = 1;
	if (*s == '-' || *s == '+')
		s++;
	while (s[i] >= '0' && s[i] <= '9' && s[i] != '\0')
	{
		*n = *n * 10 - (s[i++] - '0');
		if ((10 < i || (*n == -214748364 && '7' < s[i] && s[i] <= '9') || \
		(*n < -214748364 && '0' <= s[i] && s[i] <= '9')) && sign == -1)
			return (1);
		if ((10 < i || (*n <= -214748364 && '8' < s[i] && s[i] <= '9') || \
		(*n > 214748364 && '0' <= s[i] && s[i] <= '9')) && sign == 1)
			return (1);
	}
	if (s[i] != '\0' || i == 0)
		return (1);
	*n = *n * sign;
	return (0);
}
